package operations

import "fmt"

func Add(first, second float64) {
	fmt.Println(first + second)
}

func Subtract(first, second float64) {
	fmt.Println(first - second)
}
func Multiply(first, second float64) {
	fmt.Println(first * second)
}

func Divide(first, second float64) {
	fmt.Println(first / second)
}
